    private void btn_tambahsementaraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahsementaraActionPerformed
        String kode = txt_kodebarang.getText();
        String nama = txt_namabarang.getText();
        
        Long harga = Long.parseLong(txt_harga.getText());
        int jumlah = Integer.parseInt(txt_jumlah.getText());
        Long subtotal  = Long.parseLong(txt_subtotal.getText());
        
        Model_Barang brg = new Model_Barang();
        Model_Detai_penjualan detJual = new Model_Detai_penjualan();
        Model_Penjualan jual = new Model_Penjualan();
        Model_SemPenjualan semjual = new Model_SemPenjualan();
        
        brg.setId_produk(kode);
        brg.setNama_produk(nama);
        brg.setHarga(harga);
        
        detJual.setJumlah(jumlah);
        detJual.setSub_total(subtotal);
        
        semjual.setMod_bararang(brg);
        semjual.setMod_detpenjualan(detJual);
        
        service_sem.tambahData(semjual);
        service_det.sumTotal(detJual);
        lb_total.setText(detJual.getSub_total().toString());
        
        loadDataSementara();
        
        if(JOptionPane.showConfirmDialog(this, "Mau Tambah Barang?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            aktifBarang();
            bersihBarang();
            btn_pilih.requestFocus();
                       
        }else {
            aktifBarang();
            bersihBarang();
        }

    }//GEN-LAST:event_btn_tambahsementaraActionPerformed

