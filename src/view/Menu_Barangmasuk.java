    private void btn_hapusemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusemActionPerformed
         int index = tbl_sementara.getSelectedRow();
        if(index != -1){
            Model_SemPenjualan model = tblModel_sem.getData(tbl_sementara.convertColumnIndexToModel(index));
            if(JOptionPane.showConfirmDialog(null, "Yakin Data Akan di Hapus", 
                    "Konfirmasi", JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
                service_sem.hapusData(model);
                tblModel_sem.hapusData(index);
                loadDataSementara();
                resetForm();
                bersihBarang();
                lb_satan.setVisible(true);
                txt_satuan.setVisible(true);
                btn_tambahsementara.setVisible(true);   
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan di update");
        }
        loadDataSementara();
        resetForm();
        bersihBarang();
        lb_satan.setVisible(true);
        txt_satuan.setVisible(true);
        btn_tambahsementara.setVisible(true);
    }//GEN-LAST:event_btn_hapusemActionPerformed

